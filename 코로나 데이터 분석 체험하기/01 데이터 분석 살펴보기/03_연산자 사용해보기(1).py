Num3 = Num2 - 243

#'Num4'이라는 변수를 하나 선언하고, 이전 값에 4927819을 더한 값을 넣어줍시다.
Num4 = Num3 + 4927819

#'Answer'이라는 변수를 하나 선언하고, 이전 값에 829176을 뺀 값을 넣어줍시다.
Answer = Num4 - 829176

